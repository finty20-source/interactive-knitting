import streamlit as st
import numpy as np

st.title("üß∂ –ò–Ω—Ç–µ—Ä–∞–∫—Ç–∏–≤–Ω–æ–µ –≤—è–∑–∞–Ω–∏–µ ‚Äî –∫–∞–ª—å–∫—É–ª—è—Ç–æ—Ä —Å–≤–∏—Ç–µ—Ä–∞")

# -----------------------------
# üßæ –í–≤–æ–¥ –¥–∞–Ω–Ω—ã—Ö
# -----------------------------
st.header("–í–≤–æ–¥ –ø–∞—Ä–∞–º–µ—Ç—Ä–æ–≤")

density_stitches = st.number_input("–ü–ª–æ—Ç–Ω–æ—Å—Ç—å (–ø–µ—Ç–ª–∏ –≤ 10 —Å–º)", min_value=1, value=23)
density_rows = st.number_input("–ü–ª–æ—Ç–Ω–æ—Å—Ç—å (—Ä—è–¥—ã –≤ 10 —Å–º)", min_value=1, value=40)

neck_width_cm = st.number_input("–®–∏—Ä–∏–Ω–∞ –≥–æ—Ä–ª–æ–≤–∏–Ω—ã (—Å–º)", min_value=5, value=18)
neck_depth_cm = st.number_input("–ì–ª—É–±–∏–Ω–∞ –≥–æ—Ä–ª–æ–≤–∏–Ω—ã (—Å–º)", min_value=2, value=6)

st.write("---")

# -----------------------------
# üîÑ –ü–µ—Ä–µ—Å—á—ë—Ç —Å–º ‚Üí –ø–µ—Ç–ª–∏/—Ä—è–¥—ã
# -----------------------------
st.header("–†–∞—Å—á—ë—Ç –≥–æ—Ä–ª–æ–≤–∏–Ω—ã")

neck_stitches = int((neck_width_cm / 10) * density_stitches)
neck_rows = int((neck_depth_cm / 10) * density_rows)

st.write(f"üìè –®–∏—Ä–∏–Ω–∞ –≥–æ—Ä–ª–æ–≤–∏–Ω—ã: {neck_width_cm} —Å–º ‚Üí **{neck_stitches} –ø–µ—Ç–µ–ª—å**")
st.write(f"üìê –ì–ª—É–±–∏–Ω–∞ –≥–æ—Ä–ª–æ–≤–∏–Ω—ã: {neck_depth_cm} —Å–º ‚Üí **{neck_rows} —Ä—è–¥–æ–≤**")

# -----------------------------
# üìä –õ–æ–≥–∏–∫–∞ —É–±–∞–≤–æ–∫
# -----------------------------
def calc_round_neckline(total_stitches, total_rows):
    """
    –†–∞—Å—á—ë—Ç —É–±–∞–≤–æ–∫ –¥–ª—è –∫—Ä—É–≥–ª–æ–π –≥–æ—Ä–ª–æ–≤–∏–Ω—ã.
    total_stitches - —Å–∫–æ–ª—å–∫–æ –≤—Å–µ–≥–æ –ø–µ—Ç–µ–ª—å –Ω—É–∂–Ω–æ —É–±—Ä–∞—Ç—å
    total_rows - –≤—ã—Å–æ—Ç–∞ –≥–æ—Ä–ª–æ–≤–∏–Ω—ã –≤ —Ä—è–¥–∞—Ö
    """

    # –ü—Ä–æ–ø–æ—Ä—Ü–∏–∏ —Ä–∞—Å–ø—Ä–µ–¥–µ–ª–µ–Ω–∏—è —É–±–∞–≤–æ–∫
    percentages = [60, 20, 10, 5, 5]

    # –ü–µ—Ä–µ–≤–æ–¥–∏–º –ø—Ä–æ—Ü–µ–Ω—Ç—ã –≤ –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ –ø–µ—Ç–µ–ª—å
    decreases = [round(total_stitches * p / 100) for p in percentages]

    # –ü–ª–∞–Ω –ø–æ —Ä—è–¥–∞–º
    plan = []
    current_row = 1

    for d in decreases:
        if d > 0:
            plan.append((current_row, d))   # —É–±–∞–≤–∫–∞ –≤ —ç—Ç–æ–º —Ä—è–¥—É
        current_row += 2                    # —É–±–∞–≤–∫–∏ —á–µ—Ä–µ–∑ —Ä—è–¥

    # –û—Å—Ç–∞—Ç–æ–∫ —Ä—è–¥–æ–≤ ‚Üí –±–µ–∑ —É–±–∞–≤–æ–∫
    while current_row <= total_rows:
        plan.append((current_row, 0))
        current_row += 1

    return plan

# -----------------------------
# üìå –ò—Ç–æ–≥–æ–≤—ã–π –ø–ª–∞–Ω –≤—è–∑–∞–Ω–∏—è
# -----------------------------
st.subheader("–ü–æ—à–∞–≥–æ–≤—ã–π –ø–ª–∞–Ω –≥–æ—Ä–ª–æ–≤–∏–Ω—ã")

neck_plan = calc_round_neckline(neck_stitches, neck_rows)

for row, dec in neck_plan:
    if dec > 0:
        st.write(f"‚û°Ô∏è –†—è–¥ {row}: —É–±–∞–≤–∏—Ç—å {dec} –ø.")
    else:
        st.write(f"‚û°Ô∏è –†—è–¥ {row}: –≤—è–∑–∞—Ç—å –ø—Ä—è–º–æ (–±–µ–∑ —É–±–∞–≤–æ–∫)")

# -----------------------------
# ‚úÖ –§–∏–Ω–∞–ª—å–Ω–æ–µ —Ä–µ–∑—é–º–µ
# -----------------------------
st.write("---")
st.success(
    f"–ì–æ—Ä–ª–æ–≤–∏–Ω–∞ —Ä–∞—Å—Å—á–∏—Ç–∞–Ω–∞: –Ω—É–∂–Ω–æ —É–±–∞–≤–∏—Ç—å **{neck_stitches} –ø–µ—Ç–µ–ª—å** –∑–∞ **{neck_rows} —Ä—è–¥–æ–≤**.\n"
    "–ü–æ—Å–ª–µ–¥–Ω–∏–µ –Ω–µ—Å–∫–æ–ª—å–∫–æ —Ä—è–¥–æ–≤ –ø—Ä–æ–≤—è–∑—ã–≤–∞–µ–º –ø—Ä—è–º–æ –¥–ª—è –ø–ª–∞–≤–Ω–æ–≥–æ –∑–∞–≤–µ—Ä—à–µ–Ω–∏—è."
)
